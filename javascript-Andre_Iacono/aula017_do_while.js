// LOOP DO WHILE

console.log("Loop for ...");
for(var i = 1; i <= 10; i++){
    console.log(i);
}
console.log("\n");

console.log("Loop while ...");
let contador = 1;
while(contador <= 10){
    console.log(contador);
    contador++;
}
console.log("\n");

console.log("Loop do while ...");
contador = 1;
do{
    console.log(contador);
    contador++;
}while(contador <= 10);