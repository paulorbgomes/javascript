// Elaborar um programa que leia dois números. Calcule e informe a soma desses números.
// Entrada
let num1 = Number(prompt("Primeiro número: "));
let num2 = Number(prompt("Segundo número: "));
// Processamento
let soma = num1 + num2;
// Saída
alert(`A soma entre ${num1} e ${num2} é igual a ${soma}`);